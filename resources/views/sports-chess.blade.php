        <h3 class="mt-8 text-2xl font-bold text-center tracking-tight text-gray-600 capitalize">Board Setup</h3>
        <p class="mt-3 text-lg text-center leading-8 text-gray-500 max-w-[50vw]">
            The game is played on an 8x8 square board with alternating light and dark squares.
            Each player starts with 16 pieces: one king, one queen, two rooks, two knights, two bishops, and eight pawns.
        </p>

        <h3 class="mt-8 text-2xl font-bold text-center tracking-tight text-gray-600 capitalize">Objective</h3>
        <p class="mt-3 text-lg text-center leading-8 text-gray-500 max-w-[50vw]">
            The primary objective is to checkmate your opponent's king.
            Checkmate occurs when the opponent's king is in a position to be captured (in check), and there is no legal move to escape the threat.
        </p>

        <h3 class="mt-8 text-2xl font-bold text-center tracking-tight text-gray-600 capitalize">Turns</h3>
        <p class="mt-3 text-lg text-center leading-8 text-gray-500 max-w-[50vw]">
            Players take turns making one move at a time.
            White moves first.
        </p>

        <h3 class="mt-8 text-2xl font-bold text-center tracking-tight text-gray-600 capitalize">Movement of Pieces</h3>
        <p class="mt-3 text-lg text-center leading-8 text-gray-500 max-w-[50vw]">
            Each type of piece has its own unique way of moving:
            <br>
            King: One square in any direction.
            <br>
            Queen: Any number of squares along a rank, file, or diagonal.
            <br>
            Rook: Any number of squares along a rank or file.
            <br>
            Bishop: Any number of squares diagonally.
            <br>
            Knight: Moves in an "L" shape (two squares in one direction and then one square perpendicular).
            <br>
            Pawn: Moves forward one square but captures diagonally. Pawns have different initial and capturing moves.
        </p>

        <h3 class="mt-8 text-2xl font-bold text-center tracking-tight text-gray-600 capitalize">Special Moves</h3>
        <p class="mt-3 text-lg text-center leading-8 text-gray-500 max-w-[50vw]">
            Castling: A special move involving the king and a rook that allows them to move simultaneously under certain conditions.
            <br>
            En passant: A pawn capture that can occur under specific circumstances.
        </p>

        <h3 class="mt-8 text-2xl font-bold text-center tracking-tight text-gray-600 capitalize">Promotion</h3>
        <p class="mt-3 text-lg text-center leading-8 text-gray-500 max-w-[50vw]">
            When a pawn reaches the eighth rank, it can be promoted to any other piece (except a king).
        </p>

        <h3 class="mt-8 text-2xl font-bold text-center tracking-tight text-gray-600 capitalize">Stalemate</h3>
        <p class="mt-3 text-lg text-center leading-8 text-gray-500 max-w-[50vw]">
            If a player has no legal moves and their king is not in check, the game is a stalemate, and it is a draw.
        </p>

        <h3 class="mt-8 text-2xl font-bold text-center tracking-tight text-gray-600 capitalize">Draw Conditions</h3>
        <p class="mt-3 text-lg text-center leading-8 text-gray-500 max-w-[50vw]">
            Threefold repetition: If the same position occurs three times with the same player to move, a draw can be claimed.
            <br>
            Fifty-move rule: If 50 consecutive moves are made by both players without a pawn move or a piece capture, the game is a draw.
        </p>

        <h3 class="mt-8 text-2xl font-bold text-center tracking-tight text-gray-600 capitalize">Time Control</h3>
        <p class="mt-3 text-lg text-center leading-8 text-gray-500 max-w-[50vw]">
            Games are often played with a time limit for each player. This can be in the form of a total time for the game or a certain amount of time per move.
        </p>

        <h3 class="mt-8 text-2xl font-bold text-center tracking-tight text-gray-600 capitalize">Resignation</h3>
