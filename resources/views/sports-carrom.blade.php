            Players take turns to play. A turn consists of one or more strikes. A player wins by pocketing all of the pieces of their chosen colour first. However, neither player can win until one or other player has "covered the Queen".
            To cover the Queen, a player must pocket one of her own pieces immediately after pocketing the Queen. If the Queen is pocketed but not covered, the Queen is returned to the board. Both players normally try to cover the Queen in addition to trying to win the game because a player who wins and also covers the Queen receives bonus points.
